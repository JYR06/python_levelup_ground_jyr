# 산술 연산자
print(5 % 2)   # 나머지: 1
print(5 // 2)  # 몫: 2
print(5 ** 2)  # 거듭제곱: 25

# 비교 및 논리 연산자
print(5 > 2)   # True
print(5 == 2)  # False (같다)
print(5 != 2)  # True (다르다)

# 논리 연산자
print((3 < 5) and (7 < 5)) # False (둘 다 참이어야 True)
print((3 < 5) or (7 < 5))  # True (둘 중 하나만 참이어도 True)
print(not (3 < 5))         # False (결과 반전)

# 멤버 연산자
print('c' in 'cat')      # True
print('c' not in 'cat')  # False